package lab_5;

public class Employees2 {
	private int id;
	private String name;
	private int tenure;
	private int salary;
	
	public Employees2(int id, String name, int tenure, int salary){
		this.id = id;
		this.name = name;
		this.tenure = tenure;
		this.salary = salary;
	}
	
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public int getTenure() {
		return tenure;
	}
	public int getSalary() {
		return salary;
	}
	
}
